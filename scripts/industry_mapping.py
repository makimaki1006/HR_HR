def get_hw_job_types(sn_industry: str) -> list[tuple[str, float]]:
    """SalesNow大業界からHW大分類のリストを返す。

    Returns: [(hw_job_type, confidence), ...]
    """
    return INDUSTRY_MAPPING.get(sn_industry, [("その他", 0.3)])


def build_mapping_rows() -> list[tuple[str, str, float]]:
    """Tursoアップロード用のマッピング行を生成。

    Returns: [(sn_industry, hw_job_type, confidence), ...]
    """
    rows = []
    for sn, mappings in INDUSTRY_MAPPING.items():
        for hw, conf in mappings:
            rows.append((sn, hw, conf))
    return rows


if __name__ == "__main__":
    rows = build_mapping_rows()
    print(f"マッピング行数: {len(rows)}")
    print(f"SalesNow業界数: {len(INDUSTRY_MAPPING)}")
    hw_types = set()
    for _, hw, _ in rows:
        hw_types.add(hw)
    print(f"HW大分類カバー数: {len(hw_types)}/13")
    print(f"カバー: {sorted(hw_types)}")
